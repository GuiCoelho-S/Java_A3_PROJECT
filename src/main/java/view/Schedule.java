    private void btnListScheduleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListScheduleActionPerformed
        Scheduling s = new Scheduling();
                
        DefaultTableModel dtmPacientes = (DefaultTableModel) tableList.getModel();
        dtmPacientes.setRowCount(0);

        String sql = "SELECT * FROM tb_scheduling";
        
        ConnectionFactory factory = new ConnectionFactory();
        try (Connection c = factory.obtemConexao()){
            PreparedStatement ps = c.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while(rs.next()){
                s.setCPF_SH(rs.getString("CPF_SH"));
                s.setDATE_SH(rs.getString("DATE_SH"));
                s.setID_SH(rs.getString("ID_SH"));
                s.setNAME_SH(rs.getString("NAME_SH"));
                
      
                Object[] data = {s.getCPF_SH(), s.getDATE_SH(), s.getID_SH(), s.getNAME_SH()}; 
                dtmPacientes.addRow(data);       
            }
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnListScheduleActionPerformed

